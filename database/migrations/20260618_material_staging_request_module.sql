CREATE TABLE IF NOT EXISTS erp_material_staging_request (
  id bigint(20) NOT NULL AUTO_INCREMENT,
  staging_no varchar(50) NOT NULL,
  id_production_order bigint(20) NOT NULL,
  no_production_order varchar(30) NOT NULL,
  request_date date NOT NULL,
  required_date date DEFAULT NULL,
  request_type varchar(30) DEFAULT 'PRODUCTION_ORDER',
  plant_id int(11) DEFAULT NULL,
  plant_code varchar(20) DEFAULT NULL,
  source_storage_location_id int(11) DEFAULT NULL,
  source_storage_location varchar(30) DEFAULT NULL,
  source_storage_bin_id int(11) DEFAULT NULL,
  source_storage_bin varchar(30) DEFAULT NULL,
  destination_storage_location varchar(30) DEFAULT NULL,
  destination_area varchar(100) DEFAULT NULL,
  priority varchar(20) DEFAULT 'NORMAL',
  staging_status enum('DRAFT','REQUESTED','PICKING','STAGED','PARTIAL_ISSUE','ISSUED','CANCELLED') NOT NULL DEFAULT 'DRAFT',
  reference_no varchar(100) DEFAULT NULL,
  remarks text DEFAULT NULL,
  created_by varchar(100) DEFAULT NULL,
  created_at datetime DEFAULT CURRENT_TIMESTAMP,
  updated_by varchar(100) DEFAULT NULL,
  updated_at datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  submitted_by varchar(100) DEFAULT NULL,
  submitted_at datetime DEFAULT NULL,
  staged_by varchar(100) DEFAULT NULL,
  staged_at datetime DEFAULT NULL,
  cancelled_by varchar(100) DEFAULT NULL,
  cancelled_at datetime DEFAULT NULL,
  cancel_reason varchar(255) DEFAULT NULL,
  PRIMARY KEY (id),
  UNIQUE KEY uk_staging_no (staging_no),
  KEY idx_staging_po (id_production_order,no_production_order),
  KEY idx_staging_status_date (staging_status,request_date,required_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS erp_material_staging_request_detail (
  id bigint(20) NOT NULL AUTO_INCREMENT,
  staging_id bigint(20) NOT NULL,
  production_material_id bigint(20) NOT NULL,
  line_no int(11) DEFAULT NULL,
  material_code varchar(100) NOT NULL,
  material_name varchar(255) DEFAULT NULL,
  required_qty decimal(18,5) DEFAULT 0.00000,
  requested_qty decimal(18,5) DEFAULT 0.00000,
  staged_qty decimal(18,5) DEFAULT 0.00000,
  issued_qty_snapshot decimal(18,5) DEFAULT 0.00000,
  remaining_qty_snapshot decimal(18,5) DEFAULT 0.00000,
  available_qty_snapshot decimal(18,5) DEFAULT 0.00000,
  uom varchar(20) DEFAULT NULL,
  source_storage_location varchar(30) DEFAULT NULL,
  source_storage_bin varchar(30) DEFAULT NULL,
  stock_type varchar(20) DEFAULT 'UNRESTRICTED',
  shortage_qty decimal(18,5) DEFAULT 0.00000,
  line_status enum('OPEN','SHORTAGE','PICKING','STAGED','PARTIAL_ISSUE','ISSUED','CANCELLED') DEFAULT 'OPEN',
  remarks varchar(255) DEFAULT NULL,
  created_at datetime DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  KEY idx_staging_detail_header (staging_id,line_no),
  KEY idx_staging_detail_material (material_code),
  CONSTRAINT fk_staging_detail_header FOREIGN KEY (staging_id) REFERENCES erp_material_staging_request(id) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS erp_material_staging_request_history (
  id bigint(20) NOT NULL AUTO_INCREMENT,
  staging_id bigint(20) NOT NULL,
  status_lama varchar(30) DEFAULT NULL,
  status_baru varchar(30) NOT NULL,
  remarks varchar(255) DEFAULT NULL,
  changed_by varchar(100) DEFAULT NULL,
  changed_at datetime DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  KEY idx_staging_history_header (staging_id,changed_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

