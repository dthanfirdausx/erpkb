function erpkb_excel_last_col($columnCount) {
  return PHPExcel_Cell::stringFromColumnIndex(max(0, (int)$columnCount - 1));
}

function erpkb_excel_filter_text($filters) {
  $parts = array();
  foreach ((array)$filters as $label => $value) {
    if (function_exists('erp_export_label')) {
      $label = erp_export_label($label);
    }
    if (is_array($value)) {
      $value = implode(' / ', array_filter($value, 'strlen'));
    }
    $value = trim((string)$value);
    if ($value === '') {
      $value = function_exists('erp_export_all_text') ? erp_export_all_text() : 'Semua';
    }
    $parts[] = $label.': '.$value;
  }
  return implode(' | ', $parts);
}

if (!function_exists('erpkb_excel_number_format_code')) {
  function erpkb_excel_number_format_code($decimals) {
    if (function_exists('erp_excel_number_format')) {
      return erp_excel_number_format($decimals);
    }
    $decimals = max(0, (int)$decimals);
    return '#,##0'.($decimals > 0 ? '.'.str_repeat('0', $decimals) : '');
  }
}

function erpkb_excel_apply_standard_style($excel, $options = array()) {
  $sheet = isset($options['sheet']) ? $options['sheet'] : $excel->getActiveSheet();
  $company = isset($options['company']) ? $options['company'] : (defined('namaPT') ? namaPT : 'ERPKB');
  $title = isset($options['title']) ? $options['title'] : $sheet->getCell('A2')->getValue();
  $subtitle = isset($options['subtitle']) ? $options['subtitle'] : '';
  $headerRow = isset($options['header_row']) ? (int)$options['header_row'] : 4;
  $firstDataRow = isset($options['first_data_row']) ? (int)$options['first_data_row'] : ($headerRow + 1);
  $lastDataRow = isset($options['last_data_row']) ? (int)$options['last_data_row'] : $firstDataRow;
  $columnCount = isset($options['column_count']) ? (int)$options['column_count'] : 1;
  $lastCol = isset($options['last_col']) ? $options['last_col'] : erpkb_excel_last_col($columnCount);
  $summaryRow = isset($options['summary_row']) ? (int)$options['summary_row'] : ($headerRow - 1);
  $numericColumns = isset($options['numeric_columns']) ? (array)$options['numeric_columns'] : array();
  $decimalColumns = isset($options['decimal_columns']) ? (array)$options['decimal_columns'] : array();
  $moneyColumns = isset($options['money_columns']) ? (array)$options['money_columns'] : array();
  $filters = isset($options['filters']) ? (array)$options['filters'] : array();
  $orientation = isset($options['orientation']) ? $options['orientation'] : PHPExcel_Worksheet_PageSetup::ORIENTATION_LANDSCAPE;

  if ($lastDataRow < $firstDataRow) $lastDataRow = $firstDataRow;

  $excel->getProperties()
        ->setCreator('ERPKB')
        ->setLastModifiedBy('ERPKB')
        ->setTitle((string)$title)
        ->setSubject((string)$title)
        ->setDescription((string)$title.' export generated by ERPKB.');

  $sheet->mergeCells('A1:'.$lastCol.'1');
  $sheet->mergeCells('A2:'.$lastCol.'2');
  $sheet->setCellValue('A1', $company);
  $sheet->setCellValue('A2', $title);

  if ($subtitle === '') {
    $subtitle = function_exists('erp_export_generated_text') ? erp_export_generated_text() : 'Generated: '.date('Y-m-d H:i:s');
  }
  $sheet->setCellValue('A'.$summaryRow, erpkb_excel_filter_text($filters) ?: $subtitle);
  $sheet->mergeCells('A'.$summaryRow.':'.$lastCol.$summaryRow);

  $sheet->getStyle('A1:'.$lastCol.'1')->getFont()->setBold(true)->setSize(16)->getColor()->setRGB('FFFFFF');
  $sheet->getStyle('A2:'.$lastCol.'2')->getFont()->setBold(true)->setSize(13)->getColor()->setRGB('FFFFFF');
  $sheet->getStyle('A1:'.$lastCol.'2')->getFill()->setFillType(PHPExcel_Style_Fill::FILL_SOLID)->getStartColor()->setRGB('0F766E');
  $sheet->getStyle('A1:'.$lastCol.'2')->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_CENTER);

  $sheet->getStyle('A'.$summaryRow.':'.$lastCol.$summaryRow)->getFill()->setFillType(PHPExcel_Style_Fill::FILL_SOLID)->getStartColor()->setRGB('E0F2FE');
  $sheet->getStyle('A'.$summaryRow.':'.$lastCol.$summaryRow)->getAlignment()->setWrapText(true)->setVertical(PHPExcel_Style_Alignment::VERTICAL_TOP);

  $sheet->getStyle('A'.$headerRow.':'.$lastCol.$headerRow)->getFont()->setBold(true)->getColor()->setRGB('FFFFFF');
  $sheet->getStyle('A'.$headerRow.':'.$lastCol.$headerRow)->getFill()->setFillType(PHPExcel_Style_Fill::FILL_SOLID)->getStartColor()->setRGB('1D4ED8');
  $sheet->getStyle('A'.$headerRow.':'.$lastCol.$headerRow)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_CENTER)->setVertical(PHPExcel_Style_Alignment::VERTICAL_CENTER)->setWrapText(true);

  $sheet->getStyle('A'.$headerRow.':'.$lastCol.$lastDataRow)->getBorders()->getAllBorders()->setBorderStyle(PHPExcel_Style_Border::BORDER_THIN);
  $sheet->getStyle('A'.$firstDataRow.':'.$lastCol.$lastDataRow)->getAlignment()->setVertical(PHPExcel_Style_Alignment::VERTICAL_TOP)->setWrapText(true);
  $sheet->getStyle('A'.$firstDataRow.':A'.$lastDataRow)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_CENTER);

  if (function_exists('erp_export_status')) {
    for ($i = 0; $i < $columnCount; $i++) {
      $col = PHPExcel_Cell::stringFromColumnIndex($i);
      $headerText = trim((string)$sheet->getCell($col.$headerRow)->getValue());
      $headerKey = strtolower($headerText);
      $statusText = function_exists('erp_export_label') ? strtolower(erp_export_label('Status')) : 'status';
      if ($headerKey !== 'status' && $headerKey !== $statusText && strpos($headerKey, 'status') === false && strpos($headerKey, $statusText) === false) {
        continue;
      }
      for ($row = $firstDataRow; $row <= $lastDataRow; $row++) {
        $cellValue = $sheet->getCell($col.$row)->getValue();
        if (is_string($cellValue) && trim($cellValue) !== '') {
          $sheet->setCellValue($col.$row, erp_export_status($cellValue));
        }
      }
    }
  }

  foreach ($numericColumns as $col) {
    $useGlobalNumberFormat = !function_exists('erp_config_get') || erp_config_get('number_apply_to_excel', 'Y') === 'Y';
    $numberSettings = ($useGlobalNumberFormat && function_exists('erp_number_format_settings')) ? erp_number_format_settings() : array();
    $qtyDecimals = isset($numberSettings['qty_precision']) ? $numberSettings['qty_precision'] : 5;
    $sheet->getStyle($col.$firstDataRow.':'.$col.$lastDataRow)->getNumberFormat()->setFormatCode(erpkb_excel_number_format_code($qtyDecimals));
    $sheet->getStyle($col.$firstDataRow.':'.$col.$lastDataRow)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_RIGHT);
  }
  foreach ($decimalColumns as $col) {
    $useGlobalNumberFormat = !function_exists('erp_config_get') || erp_config_get('number_apply_to_excel', 'Y') === 'Y';
    $numberSettings = ($useGlobalNumberFormat && function_exists('erp_number_format_settings')) ? erp_number_format_settings() : array();
    $amountDecimals = isset($numberSettings['decimal_precision']) ? $numberSettings['decimal_precision'] : 2;
    $sheet->getStyle($col.$firstDataRow.':'.$col.$lastDataRow)->getNumberFormat()->setFormatCode(erpkb_excel_number_format_code($amountDecimals));
    $sheet->getStyle($col.$firstDataRow.':'.$col.$lastDataRow)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_RIGHT);
  }
  foreach ($moneyColumns as $col) {
    $useGlobalNumberFormat = !function_exists('erp_config_get') || erp_config_get('number_apply_to_excel', 'Y') === 'Y';
    $numberSettings = ($useGlobalNumberFormat && function_exists('erp_number_format_settings')) ? erp_number_format_settings() : array();
    $amountDecimals = isset($numberSettings['decimal_precision']) ? $numberSettings['decimal_precision'] : 2;
    $sheet->getStyle($col.$firstDataRow.':'.$col.$lastDataRow)->getNumberFormat()->setFormatCode(erpkb_excel_number_format_code($amountDecimals));
    $sheet->getStyle($col.$firstDataRow.':'.$col.$lastDataRow)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_RIGHT);
  }

  $sheet->setAutoFilter('A'.$headerRow.':'.$lastCol.$lastDataRow);
  $sheet->freezePane('A'.$firstDataRow);
  $sheet->getPageSetup()->setOrientation($orientation);
  $sheet->getPageSetup()->setFitToWidth(1);
  $sheet->getPageSetup()->setFitToHeight(0);
  $sheet->getPageSetup()->setRowsToRepeatAtTopByStartAndEnd($headerRow, $headerRow);
  $sheet->getPageMargins()->setTop(0.5)->setRight(0.35)->setLeft(0.35)->setBottom(0.5);
  $sheet->getRowDimension($summaryRow)->setRowHeight(32);
  $sheet->getRowDimension($headerRow)->setRowHeight(28);

  $widths = isset($options['widths']) ? (array)$options['widths'] : array();
  for ($i = 0; $i < $columnCount; $i++) {
    $col = PHPExcel_Cell::stringFromColumnIndex($i);
    if (isset($widths[$col])) {
      $sheet->getColumnDimension($col)->setWidth($widths[$col]);
    } else {
      $sheet->getColumnDimension($col)->setAutoSize(true);
    }
  }
}

function erpkb_excel_temp_file($prefix) {
  $tempDirectory = ini_get('upload_tmp_dir');
  if (!$tempDirectory || !is_dir($tempDirectory) || !is_writable($tempDirectory)) {
    $tempDirectory = '/Applications/XAMPP/xamppfiles/temp';
  }
  if (!$tempDirectory || !is_dir($tempDirectory) || !is_writable($tempDirectory)) {
    $tempDirectory = sys_get_temp_dir();
  }
  return tempnam($tempDirectory, $prefix);
}
